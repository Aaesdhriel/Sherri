	<div class="span8">
		<h4>Abstract</h4>
		<p>In this paper, I examine the empirically-based folk concept of Praise-Blame Asymmetry. I note that when laypeople make 
		assessments of moral responsibility, they sometimes appeal to the agent’s motivations for acting, and they sometimes do not. 
		In order to figure out why they make these judgements and how these judgements are justified, I construct and explore an 
		empirically-based folk theory of moral worth. While examining the folk theory of moral worth, I realize that laypeople 
		inconsistently apply the criteria for an agent’s being morally responsible and that it is these inconsistently applied 
		criteria that are fuelling asymmetrical judgements about praiseworthiness and blameworthiness. I try to resolve the 
		inconsistencies in the folk theory of moral worth by appealing to the intend/foresee distinction and, eventually, by including 
		new criteria for ascriptions of moral responsibility: reasons for action. Believing that Nomy Arpaly’s theory of moral worth 
		can successfully support Praise-Blame Asymmetry; I flesh out her theory, hoping to resolve the inconsistencies and problems 
		that I noted in the original folk theory of moral worth. </p></br>
		<h4>Keywords:</h4>
		<p>Praise-Blame Asymmetry; Responsibility; Moral Worth;</p></br>
