	<div class="span8">
		<h4>Abstract</h4>
		<p>The purpose of this paper was to answer the question of whether morally indifferent agents are blameworthy when they 
		perform wrong acts. The answer to my question is that morally indifferent agents are not blameworthy when they perform wrong 
		acts, and that we are rarely, if ever, justified in considering them blameworthy. I answered this question by looking at 
		Nomy Arpaly’s theory of moral worth. I introduced Arpaly’s view with the praise-blame asymmetry in mind. Arpaly’s theory of 
		moral worth tracks the asymmetry and attempts to justify the actual asymmetrical judgments that we make when ascribing praise 
		and blame. I used Arpaly’s view as a vehicle for demonstrating how we go wrong when we claim that morally indifferent agents 
		are blameworthy when they perform wrong acts. To reach my conclusion, I determined that Arpaly thinks that it is a sufficient 
		condition for an agent’s being blameworthy that he is insufficiently responsive to the relevant moral reasons when he performs 
		a wrong act. In response, I showed that morally indifferent agents are not actually insufficiently responsive to relevant 
		moral reasons when they act, and that they are, thus, not blameworthy.</p></br>
		<h4>Keywords:</h4>
